import pkg from "mongoose";
const { Schema, model } = pkg;

const postSchema = new Schema(
  {
    name: {
      type: String,
      required: true,
    },
    author: {
      type: String,
      required: true,
    },
    text: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const Post = model("Post", postSchema);
export default Post;
